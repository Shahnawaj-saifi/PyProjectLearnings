# Dynamically typed - Type of data that python supports

age = 35

# floating value
pi = 3.14

# String
name = "Shahnawaj"
# Boolean

isMale = True

# How to check variable type?
print(type(age))
print(type(name))
print(type(isMale))
print(type(pi))

# complex number

complex_number = 2 + 3j

print(complex_number.real)
print(complex_number.imag)




